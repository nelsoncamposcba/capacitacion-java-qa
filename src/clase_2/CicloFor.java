package clase_2;

public class CicloFor {
    public static void main(String[] args) {
        for (int index = 0; index < 3; index++){
            System.out.println(index);
        }

        // 1° ITERACION
        //index = 0
        //index++ => index = 1
        //index < 3 => 1 < 3 ==> true

        // 2° ITERACION
        //index = 1
        //index++ => index = 2
        //index < 3 => 2 < 3 ==> true

        // 3° ITERACION
        //index = 2
        //index++ => index = 3
        //index < 3 => 3 < 3 ==> false
    }
}
